#include "push_swap.h"

void	ft_exit(char *str, t_meta *meta)
{
	ft_putstr_fd("\033[31m", 2);
	ft_putstr_fd(str, 2);
	ft_putstr_fd("\033[0m\n", 2);
	if (meta && meta->p)
		ft_lstfree(meta->p);
	exit(1);
}
